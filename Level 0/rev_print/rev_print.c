#include <unistd.h>

int	main(int argc, int **argv)
{
	int	i = 0;

	if (argc == 2)
	{	
		while (argv[1][i] != '\n')
			i++;
		i--;
		while (i >= 0)
		{
			write (1, &argv[1][i], 1);
			i--;
		}
	}
	write (1, "\n", 1);
	return (0);
}